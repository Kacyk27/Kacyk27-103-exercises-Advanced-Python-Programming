reversed={val:key for key,val in MORSE_CODE.items()}
def encrypt(message):
    return ' '.join([MORSE_CODE[char.upper()] if char != ' ' else '/' for char in message])

def decrypt(message):


    message=message.split()
    result = ""
    for i in message:
        if i in MORSE_CODE.values():
            result += reversed[i]

        else:
            result += " "
    return result

